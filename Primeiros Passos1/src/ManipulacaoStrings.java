public class ManipulacaoStrings {
    public static void main(String[] args) {
        String texto = "Estrutura de Dados em Java";

        System.out.println("Texto original: " + texto);
        System.out.println("Comprimento: " + texto.length());
        System.out.println("Em maiúsculas: " + texto.toUpperCase());
        System.out.println("Em minúsculas: " + texto.toLowerCase());
    }
}
